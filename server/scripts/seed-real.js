async function seedDatabase() {
  try {
    await mongoose.connect(MONGO_URI)
    console.log('✅ MongoDB connected')
    
    // 清空现有数据
    await House.deleteMany({})
    console.log('🗑️  已清空旧数据')
    
    // 插入新数据
    const result = await House.insertMany(SEED_HOUSES)
    console.log(`✅ 成功插入 ${result.length} 条房源数据`)
    
    // 统计各区数量
    const stats = {}
    result.forEach(h => {
      stats[h.district] = (stats[h.district] || 0) + 1
    })
    console.log('\n📊 各区房源数量:')
    Object.entries(stats).forEach(([district, count]) => {
      console.log(`   ${district}: ${count} 套`)
    })
    
    process.exit(0)
  } catch (err) {
    console.error('❌ 种子数据插入失败:', err)
    process.exit(1)
  }
}

seedDatabase()
